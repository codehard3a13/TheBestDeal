    private void cmd_enregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_enregisterActionPerformed
        
        if      (txt_name.getText().equals("") || 
                 txt_prenom.getText().equals("") ||  
                 txt_login.getText().equals("") ||
                 PasswordField_pwd.getText().equals("") ||
                 txt_adresse.getText().equals("") ||
                 txt_email.getText().equals("") ||
                 txt_telephone.getText().equals("")
                )
        {
           JOptionPane.showMessageDialog( null, "Veuillez completer les champs vides" ); 
        }
        else if (txt_telephone.getText().matches("[a-z]*"+"[A-Z]*"))
        {
        JOptionPane.showMessageDialog( null, "Veuillez v√©rifier votre num√©ro de telephone !!!" );
        txt_telephone.setText(""); 
        }
        else
        {
        AdministrateurDAO d = new AdministrateurDAO();
        Administrateur a=new Administrateur();
        a.setNom(txt_name.getText());
        a.setPrenom(txt_prenom.getText());
        a.setLogin(txt_login.getText());
        a.setPwd(PasswordField_pwd.getText());
        a.setEmail(txt_email.getText());
        a.setAdresse(txt_adresse.getText());
        a.setTelephone(Integer.parseInt(txt_telephone.getText()));
        d.updateAdmin(a);  
        JOptionPane.showMessageDialog( null, "Modification enregistr√©e !!!" );
        this.dispose();
        }
        
        
        
        
               
        
    }//GEN-LAST:event_cmd_enregisterActionPerformed

